def box_ciou(box1, box2):
    # Returns tsqrt_he IoU of box1 to box2. box1 is 4, box2 is nx4
    x1y1x2y2 = False
    GIoU = False
    DIoU = False
    CIoU = True
    alpha = 2
    eps = 1e-9
    # box2 = tf.transpose(box2)
    # box2 = box2.T

    # Get the coordinates of bounding boxes
    if x1y1x2y2:  # x1, y1, x2, y2 = box1
        b1_x1, b1_y1, b1_x2, b1_y2 = box1[0], box1[1], box1[2], box1[3]
        b2_x1, b2_y1, b2_x2, b2_y2 = box2[0], box2[1], box2[2], box2[3]
    else:  # transform from xywh to xyxy
        b1_x1, b1_x2 = box1[0] - box1[2] / 2, box1[0] + box1[2] / 2
        b1_y1, b1_y2 = box1[1] - box1[3] / 2, box1[1] + box1[3] / 2
        b2_x1, b2_x2 = box2[0] - box2[2] / 2, box2[0] + box2[2] / 2
        b2_y1, b2_y2 = box2[1] - box2[3] / 2, box2[1] + box2[3] / 2

    # Intersection area
    inter = (K.minimum(b1_x2, b2_x2) - K.maximum(b1_x1, b2_x1)) * \
            (K.minimum(b1_y2, b2_y2) - K.maximum(b1_y1, b2_y1))

    # Union Area
    w1, h1 = b1_x2 - b1_x1, b1_y2 - b1_y1 + eps
    w2, h2 = b2_x2 - b2_x1, b2_y2 - b2_y1 + eps
    union = w1 * h1 + w2 * h2 - inter + eps

    # change iou into pow(iou+eps)
    # iou = inter / union

    iou = K.pow(inter/union + eps, alpha)
    beta = 2 * alpha
    if GIoU or DIoU or CIoU:
        cw = K.maximum(b1_x2, b2_x2) - K.minimum(b1_x1, b2_x1)  # convex (smallest enclosing box) width
        ch = K.maximum(b1_y2, b2_y2) - K.minimum(b1_y1, b2_y1)  # convex height
        if CIoU or DIoU:  # Distance or Complete IoU https://arxiv.org/abs/1911.08287v1
            c2 = cw ** beta + ch ** beta + eps  # convex diagonal
            rho_x = K.abs(b2_x1 + b2_x2 - b1_x1 - b1_x2)
            rho_y = K.abs(b2_y1 + b2_y2 - b1_y1 - b1_y2)
            rho2 = (rho_x ** beta + rho_y ** beta) / (2 ** beta)  # center distance
            if DIoU:
                return iou - rho2 / c2  # DIoU
            elif CIoU:  # https://github.com/Zzh-tju/DIoU-SSD-pyK/blob/master/utils/box/box_utils.py#L47
                v = (4 / math.pi ** 2) * K.pow(tf.atan(w2 / h2) - tf.atan(w1 / h1), 2)

                alpha_ciou = v / ((1 + eps) - inter / union + v)
                # return iou - (rho2 / c2 + v * alpha_ciou)  # CIoU
                return iou - (rho2 / c2 + K.pow(v * alpha_ciou + eps, alpha))  # CIoU
        else:  # GIoU https://arxiv.org/pdf/1902.09630.pdf
            # c_area = cw * ch + eps  # convex area
            # return iou - (c_area - union) / c_area  # GIoU
            c_area = K.max(cw * ch + eps, union) # convex area
            return iou - K.pow((c_area - union) / c_area + eps, alpha)  # GIoU
    else:

        return iou # K.log(iou+eps) or iou
